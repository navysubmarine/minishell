#include "minishell.h"

void    ft_echo(char *rl)
{
    int i;
    i = 0;
    if (!rl)
        return ;
    while (rl[i])
    {
        write(1, &(rl[i]), 1);
        i++;
    }
    write (1, "\n", 1);
}

