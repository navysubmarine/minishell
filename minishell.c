int	main(void)
{
	// char		*cwd;
	t_global	g;

	//print_banner();
	while (1)
	{
		init_global_struct(&g);

		// cwd = getcwd(NULL, 0);
		// if (!cwd)
		// {
		// 	ft_printf("Error: getcwd failed\n");
		// 	return (1);
		// }
		g.input = readline("‚ėÖminihell‚ėÖ>");
		//free(cwd);
		if (!g.input)
		{
			ft_printf("Error : readline failed\n");
			continue ;
		}
		tokenize(&g);
		if (!check_tokens(g.tok_list))
			continue ;	
		if (g.tok_list)
		{
			ft_printf("Tokens:\n");
			token_print(g.tok_list);
			token_clear(&g.tok_list);
		}
		free(g.input);
		rl_clear_history();
	}
	return (0);
}
